from __future__ import annotations

from os import PathLike
from pathlib import Path
from typing import (
    Dict,
    List,
    Union,
    TYPE_CHECKING,
)

if TYPE_CHECKING:
    import numpy as np
    from numpy.typing import NDArray

    from ..typing import (
        AnnualGroupResult,
        AnnualGridData,
        AnnualMetricResults,
        AnnualRawResults,
        AnnualResult,
        SensorGridInfo,
    )

from ..utils import load_npy
from .common import (
    check_sensor_count,
    grid_count,
    grid_identifier,
    grid_infos,
    load_grid_res,
)


def read_npy_folder(
    folder: Path,
    grid_info_values: List["SensorGridInfo"],
) -> Dict[str, "NDArray[np.float32]"]:
    """Read annual daylight ``.npy`` files by sensor grid identifier.

    Args:
        folder: Folder containing one ``.npy`` matrix per sensor grid.
        grid_info_values: Parsed values from ``grids_info.json``.

    Returns:
        Sensor grid identifiers mapped to annual result matrices.
    """
    values: Dict[str, "NDArray[np.float32]"] = {}
    for grid_info in grid_info_values:
        grid_id = grid_identifier(grid_info)
        npy_file = folder / f"{grid_id}.npy"
        if not npy_file.is_file():
            raise FileNotFoundError(
                f"Missing annual daylight npy file: {npy_file}"
            )
        array = load_npy(npy_file)
        check_sensor_count(
            array,
            grid_count(grid_info),
            npy_file,
        )
        values[grid_id] = array

    return values


def read_annual_group(
    aperture_group: "AnnualGroupResult",
    grids_info_folder: Union[str, PathLike[str]],
) -> "AnnualGridData":
    """Read one annual daylight aperture group.

    Args:
        aperture_group: Annual daylight aperture group result metadata.
        grids_info_folder: Folder containing ``grids_info.json``.

    Returns:
        Total and direct illuminance matrices keyed by sensor grid identifier.
    """
    grid_info_values = grid_infos(Path(grids_info_folder))
    return {
        "total": read_npy_folder(
            aperture_group["total_dir"],
            grid_info_values,
        ),
        "direct": read_npy_folder(
            aperture_group["direct_dir"],
            grid_info_values,
        ),
    }


def read_annual_raw(
    result: "AnnualResult",
) -> "AnnualRawResults":
    """Read raw annual daylight total and direct matrices.

    Args:
        result: Annual daylight simulation result metadata.

    Returns:
        Aperture groups mapped to total and direct sensor grid matrices.
    """
    return {
        group_id: read_annual_group(
            aperture_group,
            result["results"],
        )
        for group_id, aperture_group in result["aperture_groups"].items()
    }


def read_metric_folder(
    metric_folder: Union[str, PathLike[str]],
    extension: str,
) -> Dict[str, "NDArray[np.float32]"]:
    """Read one official annual metric folder.

    Args:
        metric_folder: Folder containing annual metric result files.
        extension: Metric file extension.

    Returns:
        Sensor grid identifiers mapped to metric arrays.
    """
    folder = Path(metric_folder)
    if not folder.is_dir():
        raise ValueError(
            f"Invalid annual metric folder: {folder}"
        )

    values: Dict[str, "NDArray[np.float32]"] = {}
    for grid_info in grid_infos(folder):
        grid_id = grid_identifier(grid_info)
        metric_file = folder / f"{grid_id}.{extension}"
        if not metric_file.is_file():
            raise FileNotFoundError(
                f"Missing annual metric file: {metric_file}"
            )

        values[grid_id] = load_grid_res(
            metric_file,
            grid_count(grid_info),
            grid_info.get(
                "start_ln",
                0,
            ),
        )

    return values


def read_annual_metrics(
    result: "AnnualResult",
) -> "AnnualMetricResults":
    """Read official metrics generated by the annual daylight recipe.

    Args:
        result: Annual daylight simulation result metadata.

    Returns:
        Official DA, cDA, UDI, lower UDI, and upper UDI results.
    """
    return {
        "da": read_metric_folder(
            result["metrics"] / "da",
            "da",
        ),
        "cda": read_metric_folder(
            result["metrics"] / "cda",
            "cda",
        ),
        "udi": read_metric_folder(
            result["metrics"] / "udi",
            "udi",
        ),
        "udi_lower": read_metric_folder(
            result["metrics"] / "udi_lower",
            "udi",
        ),
        "udi_upper": read_metric_folder(
            result["metrics"] / "udi_upper",
            "udi",
        ),
    }
